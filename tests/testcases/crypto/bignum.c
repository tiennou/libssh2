#include "clar_libssh2.h"

static LIBSSH2_SESSION *g_session = NULL;

void test_crypto_bignum__initialize(void)
{
    g_session = cl_ssh2_open_session(NULL, 1);
}

void test_crypto_bignum__cleanup(void)
{
    cl_ssh2_close_connected_session();
}

void test_crypto_bignum__new(void)
{
    _libssh2_bn *bn = _libssh2_bn_new();

    cl_assert(bn != NULL);

    _libssh2_bn_free(bn);
}

void test_crypto_bignum__set_word(void)
{
    _libssh2_bn *bn = _libssh2_bn_new();
    unsigned char buf[10];

    cl_assert(bn != NULL);
    _libssh2_bn_set_word(bn, 2);

    cl_assert_equal_i(2, _libssh2_bn_bits(bn));
    cl_assert_equal_i(1, _libssh2_bn_bytes(bn));

    _libssh2_bn_to_bin(bn, (unsigned char *)&buf);
    cl_assert_equal_i(2, buf[0]);

    _libssh2_bn_free(bn);
}

void test_crypto_bignum__from_bin(void)
{
    static const unsigned char p_value[128] = {
        0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
        0xC9, 0x0F, 0xDA, 0xA2, 0x21, 0x68, 0xC2, 0x34,
        0xC4, 0xC6, 0x62, 0x8B, 0x80, 0xDC, 0x1C, 0xD1,
        0x29, 0x02, 0x4E, 0x08, 0x8A, 0x67, 0xCC, 0x74,
        0x02, 0x0B, 0xBE, 0xA6, 0x3B, 0x13, 0x9B, 0x22,
        0x51, 0x4A, 0x08, 0x79, 0x8E, 0x34, 0x04, 0xDD,
        0xEF, 0x95, 0x19, 0xB3, 0xCD, 0x3A, 0x43, 0x1B,
        0x30, 0x2B, 0x0A, 0x6D, 0xF2, 0x5F, 0x14, 0x37,
        0x4F, 0xE1, 0x35, 0x6D, 0x6D, 0x51, 0xC2, 0x45,
        0xE4, 0x85, 0xB5, 0x76, 0x62, 0x5E, 0x7E, 0xC6,
        0xF4, 0x4C, 0x42, 0xE9, 0xA6, 0x37, 0xED, 0x6B,
        0x0B, 0xFF, 0x5C, 0xB6, 0xF4, 0x06, 0xB7, 0xED,
        0xEE, 0x38, 0x6B, 0xFB, 0x5A, 0x89, 0x9F, 0xA5,
        0xAE, 0x9F, 0x24, 0x11, 0x7C, 0x4B, 0x1F, 0xE6,
        0x49, 0x28, 0x66, 0x51, 0xEC, 0xE6, 0x53, 0x81,
        0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
    };
    unsigned char buf[128];
    _libssh2_bn *p;
    size_t pos = 0;

    p = _libssh2_bn_new_from_bin(sizeof(p_value), p_value);
    cl_assert(p != NULL);
    cl_assert_equal_i(1024, _libssh2_bn_bits(p));
    cl_assert_equal_i(128, _libssh2_bn_bytes(p));

    _libssh2_bn_to_bin(p, (unsigned char *)&buf);
    while(pos < _libssh2_bn_bytes(p)) {
        cl_assert_equal_i(p_value[pos], buf[pos]);
        pos++;
    }

    _libssh2_bn_free(p);
}
